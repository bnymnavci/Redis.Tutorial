namespace RedisTutorial.Notes;

internal class InMemoryCacheNotes
{
    /*
     * Absolute Time Nedir?
     * Cache'deki datanın ne kadar tutulacağına dair net ömrünün belirtilmesidir. Belirtilen ömür sona erdiğinde cache direkt olarak temizlenir.
     * 
     * 
     * Sliding Time Nedir?
     * Cache'lenmiş datanın memory'de belirtilen süre periyodu zarfında tutulmasını belirtir. Belirtilen süre periyodu cache'e yapılan erişim 
     * neticesinde de datanın ömrü bir o kadar uzatılacaktır. Aksi takdirde belirtilen süre zargında bir erişim söz konusu olmazsa cache temizlenecektir.
     * 
     */
}
